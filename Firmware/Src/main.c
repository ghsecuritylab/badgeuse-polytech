/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint32_t time_ref;
	uint8_t card_uid[ISO_14443_4_UID_SIZE];
	uint8_t uid_size;
	RTC_TimeTypeDef rtctime;
	RTC_DateTypeDef rtcdate;

	Scan sc =
	{
			.card_uid = {0},
			.timestamp = 0
	};

	ip4_addr_t ntp_server_ip;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_LWIP_Init();
  MX_SPI1_Init();
  MX_RTC_Init();
  MX_TIM2_Init();
  MX_WWDG_Init();
  /* USER CODE BEGIN 2 */
	init_CYC_Timer();
	PCD_Init();
	fifoScanInit();
	printf("Hello world !\r\n");
	sntp_setoperatingmode(SNTP_OPMODE_POLL);
	IP4_ADDR(&ntp_server_ip, 51, 255, 141, 154);
	sntp_setserver(0, &ntp_server_ip);
	sntp_init();

	mark_CYC_Timer(&time_ref);
	HAL_TIM_Base_Start_IT(&htim2);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
		MX_LWIP_Process();
		DB_Process();
		beepProcess();



		if(getCardUid(card_uid, &uid_size)>0 && CYC_TO_US(elapsed_CYC_Timer(time_ref))>=2000000)
		{
			HAL_RTC_GetTime(&hrtc, &rtctime, RTC_FORMAT_BIN);
			HAL_RTC_GetDate(&hrtc, &rtcdate, RTC_FORMAT_BIN);
			printf("%d:%d:%d\n\n", rtctime.Hours, rtctime.Minutes, rtctime.Seconds);
			sc.timestamp = 0;
			sc.timestamp = (rtcdate.Year & 0x7F) << 20 | (rtcdate.Month & 0x0F) << 16 | (rtcdate.Date & 0x1F) << 11 | ((rtctime.Hours) & 0x1F) << 6 | (rtctime.Minutes & 0x3F);
			memcpy(sc.card_uid, card_uid, ISO_14443_4_UID_SIZE);
			fifoScanPut(&sc);

			HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
			beep(15000, 200);
			mark_CYC_Timer(&time_ref);
		}
		else if(CYC_TO_US(elapsed_CYC_Timer(time_ref))>=1000000)
		{
			HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
		}


		HAL_WWDG_Refresh(&hwwdg);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	}
  /* USER CODE END 3 */
}

